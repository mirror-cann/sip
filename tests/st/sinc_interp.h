void BaseBndSinc32by16_float(complex<float> *inSignal, long long SigLen, double SigSta, double SigStep, double *IntpPos,
                             long long IntpLen, complex<float> *intpDat)
{
    int N_core = 16;
    int N_quanti = 32;

    complex<float> *Signal = new complex<float>[SigLen + N_core];
    memset((void *)Signal, 0, sizeof(complex<float>) * (SigLen + N_core));
    memcpy(Signal + N_core / 2, inSignal, sizeof(complex<float>) * SigLen);
    //	memcpy(Signal, inSignal+SigLen-N_core/2, sizeof(complex<float>)*N_core/2);
    //	memcpy(Signal+SigLen+N_core/2, inSignal, sizeof(complex<float>)*N_core/2);
    for (int i = 0; i < N_core / 2; i++)
        Signal[i] = inSignal[0];
    for (int i = SigLen + N_core - N_core / 2; i < SigLen + N_core; i++)
        Signal[i] = inSignal[SigLen - 1];

    // complex<float>* intpDat = new complex<float>[IntpLen]();

    //将头尾不在数据范围内的插值不处理
    int startPos = 0, endPos = IntpLen - 1;
    for (int i = 0; i < IntpLen; i++) {
        double rem_value = (IntpPos[i] - SigSta) / SigStep;
        int rem_int = floor(rem_value) + 1;  //%% add "+1"
        if (rem_int > SigLen || rem_int < 1) {
            std::cout << "i : " << i << " rem_value : " << rem_value << " rem_int : " << rem_int << std::endl;
            startPos++;
            continue;
        } else {
            break;
        }
    }

    for (int i = IntpLen - 1; i >= startPos; i--) {
        double rem_value = (IntpPos[i] - SigSta) / SigStep;
        int rem_int = floor(rem_value) + 1;  //%%add "+1"

        if (rem_int > SigLen || rem_int < 1) {
            endPos--;
            continue;
        } else {
            break;
        }
    }

    for (int i = startPos; i <= endPos; i++) {
        double rem_value = (IntpPos[i] - SigSta) / SigStep;
        int rem_int = floor(rem_value) + 1;  //

        double mod_value = rem_value - floor(rem_value);
        if (mod_value > 1) {
            std::cout << "i : " << i << std::endl;
        }
        int core_row = round(mod_value * N_quanti);

        int core_column = rem_int + N_core / 2;

        for (int j = rem_int - N_core / 2, ii = 0; j < core_column; j++, ii++) {
            complex<double> temSignal = (complex<double>)Signal[j + N_core / 2];
            complex<double> temIntpDat = (complex<double>)intpDat[i];
            // complex<double> rtVal = temIntpDat + temSignal*DINTER_CORE_33x16[ii*33+core_row/*core_column*33+ii*/];
            complex<double> rtVal = temIntpDat + temSignal * DINTER_CORE_33x16[core_row * 16 + ii];
            intpDat[i] = rtVal;
        }
    }
    delete[] Signal;
}
